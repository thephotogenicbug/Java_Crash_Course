public class UnBoxing {

    public static void main(String[] args){

        Integer a = 100;
        Double b = 90.9876;
        Character c = 'h';

        // UnBoxing
        int x = a;
        double y = b;
        char z = c;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
    }
}
